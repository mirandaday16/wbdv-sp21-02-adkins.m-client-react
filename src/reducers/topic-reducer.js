const initialState = {
    topics: [
        {_id: "123", title: "Topic A"},
        {_id: "456", title: "Topic B"},
        {_id: "789", title: "Topic C"},
    ]
}

const topicReducer = (state=initialState, action) => {
    switch (action.type) {
        case 'CREATE_TOPIC':
            return {
                topics: [
                    ...state.topics,
                    {
                        title: "New Topic",
                        _id: (new Date()).getTime() // TODO: remember to remove this line; id should be generated by server!
                    }
                ]
            }
        case 'DELETE_TOPIC':
            return {
                topics: state.topics.filter(topic => {
                    return topic._id !== action.topicToDelete._id;
                })
            }
        case 'UPDATE_TOPIC':
            return {
                topics: state.topics.map(topic => {
                    if(topic._id === action.topic._id) {
                        return action.topic
                    } else {
                        return topic
                    }
                })
            }
        default:
            return state
    }
}

export default topicReducer